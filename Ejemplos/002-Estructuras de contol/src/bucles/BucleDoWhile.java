package bucles;

import java.util.Random;

public class BucleDoWhile {

    public static void main(String[] args) {
        Random random = new Random();
        int numeroEstudiantes = 15;
        // contador manual
        int i = 1;

        do {
            // genera notas entre -1 y 10
            int nota = random.nextInt(12) - 1;
            System.out.println("Estudiante " + i + " tiene nota: " + nota);

            // Saltar notas inv√°lidas
            if (nota < 0) {
                System.out.println("Nota inv√°lida, se ignora.\n");
                i++;
                // pasa a la siguiente iteraci√≥n
                continue;
            }

            // Detener si encontramos un 10
            if (nota == 10) {
                System.out.println("¬°Nota perfecta encontrada! Deteniendo el bucle.\n");
                // sale del bucle
                break;
            }

            // Clasificaci√≥n con switch expresi√≥n
            String calificacion = switch (nota) {
                case 0,1,2,3,4 -> "Suspenso ‚ùå";
                case 5 -> "Aprobado ‚úîÔ∏è";
                case 6 -> "Bien üôÇ";
                case 7,8 -> "Notable üëç";
                case 9 -> "Sobresaliente üåü";
                default -> "Nota no v√°lida";
            };

            System.out.println("Calificaci√≥n: " + calificacion + "\n");

            // incrementar al final
            i++;

        } while (i <= numeroEstudiantes);
    }
}
