package models;

/**
 * Basic structure of a class.
 * We also can see different types of comments.
 */
public class Animal {

    //attributes
    private String name;

    /*
     * Getter method to get the animal's name.
     */
    public String getName() {
        return name;
    }

    /*
     * Setter method to set the animal's name
     */
    public void setName(String name) {
        this.name = name;
    }
}
