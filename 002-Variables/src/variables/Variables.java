package variables;

/**
 * @author jorge
 *
 */
public class Variables {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		//declaracion de variables
		int numero;
		char letra;
		
		//inicializacion de variables
		numero = 7;
		letra = 'C';
		
		//declaracion e inicializacion de variables al mismo tiempo
		int numA = 8;
		char letraA = 'A';
		
		//declaracion e inicializacion de constantes
		final int NUMHORAS = 8;
		
		//camelCase para palabras compuestas
		int otroNumero = 9;
		
		//impresion por pantalla
		System.out.println("numero : " + numero);
		System.out.println("numA : " + numA);
		System.out.println("otroNumero : " + otroNumero);
		System.out.println("letra : " + letra);
		System.out.println("letraA :" + letraA);
		System.out.println("NUMHORAS : " + NUMHORAS);
		
		
	}

}
