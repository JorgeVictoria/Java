public class NumericosEnteros {

    public static void main(String[] args) {

        //tipo byte
        System.out.println("Tipo Byte");
        System.out.println("------------");
        byte enteroByte = 7;
        System.out.println("Min. byte = " + Byte.MIN_VALUE);
        System.out.println("Max. byte = " + Byte.MAX_VALUE);
        System.out.println("Num. bites = " + Byte.BYTES);
        System.out.println("Num. bits = " + Byte.SIZE);
        System.out.println();

        //tipo short
        System.out.println("Tipo Short");
        System.out.println("------------");
        short enteroShort = 7;
        System.out.println("Min. short = " + Short.MIN_VALUE);
        System.out.println("Max. short = " + Short.MAX_VALUE);
        System.out.println("Num. bites = " + Short.BYTES);
        System.out.println("Num. bits = " + Short.SIZE);
        System.out.println();

        //tipo int
        System.out.println("Tipo Int");
        System.out.println("------------");
        int enteroInt = 7;
        System.out.println("Min. int = " + Integer.MIN_VALUE);
        System.out.println("Max. int = " + Integer.MAX_VALUE);
        System.out.println("Num. bites = " + Integer.BYTES);
        System.out.println("Num. bits = " + Integer.SIZE);
        System.out.println();

        //tipo long
        System.out.println("Tipo Long");
        System.out.println("------------");
        long enteroLong = 7L;
        System.out.println("Min. Long = " + Long.MIN_VALUE);
        System.out.println("Max. Long = " + Long.MAX_VALUE);
        System.out.println("Num. bites = " + Long.BYTES);
        System.out.println("Num. bits = " + Long.SIZE);
        System.out.println();

    }
}
