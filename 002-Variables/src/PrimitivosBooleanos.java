public class PrimitivosBooleanos {
    public static void main(String[] args) {

        //valores literales
        boolean verdadero = true;
        boolean falso = false;
        System.out.println("falso = " + falso);
        System.out.println("verdadero = " + verdadero);

        //valores contantes
        boolean verdaderoB = Boolean.TRUE;
        boolean falsoB = Boolean.FALSE;
        System.out.println("verdaderoB = " + verdaderoB);
        System.out.println("falsoB = " + falsoB);

    }
}
